#include <stdlib.h>
#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*node;

	if (lst != NULL && f != NULL)
	{
		f(lst);
		node = lst->next;
		while (node != NULL)
		{
			f(node);
			node = node->next;
		}
	}
}
